// Создать произвольную переменную, присвоить ей произвольное строковое значение. C помощью if написать условие: если переменная равна “hidden”, присвоить ей значение “visible”, иначе - “hidden”.

let text = 'visible';

if (text === 'hidden'){
    text = 'visible';
}else{
    text = 'hidden';
}

alert(text);